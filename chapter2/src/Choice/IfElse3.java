package Choice;

public class IfElse3 {

	public static void main(String[] args) {
		// 숫자 비교(일치)
		int a = 100;
		//int b = 200;
		int b = 100;
		
		if(a == b) {
			System.out.println("두 수가 일치합니다.");
		}else {
			System.out.println("두 수가 일치하지 않습니다.");
		}
		// 문자 비교(일치)
		String str1 = "사과";
		String str2 = "바나나";
		
		//equals() - 함수 사용
		if(str1.equals(str2)) {
			System.out.println("두 단어가 일치합니다.");
		}else{
			System.out.println("두 단어가 일치하지 않습니다.");
		}
		System.out.println();
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
